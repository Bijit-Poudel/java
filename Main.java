public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!!!!!!");
        String a = "Bijit"; // String declares that my variable "a" would store a string
        int b = 1000; // int declares that my variable "b" would store an integer
        char c = 'a'; // char declares that my variable "c" would store a character
        float d = 209.02f; // float declares that my variable "d" would store a floating number
        boolean B = true; // boolean declares that my variable "B" would store a boolean value i.e true or false
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(B);
    }
}